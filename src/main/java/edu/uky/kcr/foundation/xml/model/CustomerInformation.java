package edu.uky.kcr.foundation.xml.model;

import com.thoughtworks.xstream.annotations.XStreamAlias;

@XStreamAlias("CustomerInformation")
public class CustomerInformation
{

	@XStreamAlias("ReferenceID")
	private String referenceId = null;
	
	@XStreamAlias("CSN")
	private String csn = null;
	
	@XStreamAlias("TRF")
	private String trf = null;
	
	@XStreamAlias("MRN")
	private String mrn = null;
	
	@XStreamAlias("PhysicianId")
	private String physicianId = null;
	
	public String getReferenceId() {
		return referenceId;
	}

	public void setReferenceId(String referenceId) {
		this.referenceId = referenceId;
	}

	public String getCsn() {
		return csn;
	}

	public void setCsn(String csn) {
		this.csn = csn;
	}

	public String getTrf() {
		return trf;
	}

	public void setTrf(String trf) {
		this.trf = trf;
	}

	public String getMrn() {
		return mrn;
	}

	public void setMrn(String mrn) {
		this.mrn = mrn;
	}

	public String getPhysicianId() {
		return physicianId;
	}

	public void setPhysicianId(String physicianId) {
		this.physicianId = physicianId;
	}

}